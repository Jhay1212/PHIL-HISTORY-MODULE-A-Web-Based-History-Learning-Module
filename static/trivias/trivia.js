const categoryButtons = document.querySelectorAll('.category');
const gameBoard = document.getElementById('gameBoard');
const questionModal = document.getElementById('questionModal');
const questionText = document.getElementById('questionText');
const answerInput = document.getElementById('answerInput');
const submitAnswerButton = document.getElementById('submitAnswer');
const feedback = document.getElementById('feedback');
const closeButton = document.getElementById('closeButton');
const selectC = document.querySelector('#select-category');
const categoriesContainer = document.querySelector('.categories');
let currentCategory = '';
let currentQuestionIndex = 0;

categoryButtons.forEach(button => {
    button.addEventListener('click', (e) => {
        currentCategory = button.dataset.category;
        // categoriesContainer.style.display = 'none';
        categoriesContainer.classList.add('category-left');
        selectC.textContent = currentCategory.toUpperCase();
        loadCategoryQuestions(currentCategory);
    });
});

function loadCategoryQuestions(category) {
    gameBoard.innerHTML = '';
    const questions = trivia[category];
    categories
    questions.forEach((question, index) => {
        const questionButton = document.createElement('button');
        questionButton.classList.add('question-button');
        questionButton.textContent = `Question ${index + 1}`;
        questionButton.addEventListener('click', () => {
            currentQuestionIndex = index;
            showQuestionModal(questions[index].question);
        });
        gameBoard.appendChild(questionButton);
    });
}

function showQuestionModal(question) {
    questionText.textContent = question;
    answerInput.value = '';
    feedback.textContent = '';
    questionModal.style.display = 'block';
}

closeButton.addEventListener('click', () => {
    questionModal.style.display = 'none';
});

submitAnswerButton.addEventListener('click', checkAnswer);

answerInput.addEventListener('keypress', function (e) {
    if (e.key === 'Enter') {
        checkAnswer();
    }
});

function checkAnswer() {
    const userAnswer = answerInput.value.trim().toLowerCase();
    const correctAnswer = trivia[currentCategory][currentQuestionIndex].trivia.toLowerCase();

    if (userAnswer === correctAnswer) {
        feedback.textContent = 'Correct!';
    } else {
        feedback.textContent = `Incorrect. The correct answer is ${trivia[currentCategory][currentQuestionIndex].trivia}.`;
    }
}
